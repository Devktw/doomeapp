package com.mstudio.android.doome.app.model;

public class JSONEp {
    private String ep_name;
    private String ep_image;
	private String ep_valus;
	private String ep;
	private String ep_url;
	
	
	
	
	public String getEp_url() {
        return ep_url;
    }

    public void setEp_url(String ep_url) {
        this.ep_url = ep_url;
    }
	public String getEp() {
        return ep;
    }

    public void setEp(String ep) {
        this.ep = ep;
    }
	public String getEp_name() {
        return ep_name;
    }

    public void setEp_name(String ep_name) {
        this.ep_name = ep_name;
    }
	public String getEp_valus() {
        return ep_valus;
    }

    public void setEp_valus(String ep_valus) {
        this.ep_valus = ep_valus;
    }
	public String getEp_image() {
        return ep_image;
    }

    public void setEp_image(String ep_image) {
        this.ep_image = ep_image;
    }
	

    
}
